    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cantidad=jTable1.getRowCount();
        Saldos saldo;
        Boolean sel=false;
        Integer id;
        Double ss;
        Double tt;
        Double rr;
        String venc=null;
        for(int i=0; i< cantidad;i++){
            sel=(Boolean) jTable1.getValueAt(i, 0);
            if(sel){
            saldo=new Saldos();
            id=Integer.parseInt(String.valueOf(jTable1.getValueAt(i,1)));
            saldo.setIdResumen(id);
            venc=String.valueOf(jTable1.getValueAt(i,2));
            saldo.setVencimientoString(venc);
            ss=Numeros.ConvertirStringADouble(String.valueOf(jTable1.getValueAt(i,3)));
            saldo.setSaldo(ss);
            rr=Numeros.ConvertirStringADouble(String.valueOf(jTable1.getValueAt(i,4)));
            saldo.setRecargo(rr);
            tt=Numeros.ConvertirStringADouble(String.valueOf(jTable1.getValueAt(i,5)));
            saldo.setTotal(tt);
            listadoSeleccion.add(saldo);
            }
        }
        System.out.println("cantidad elegidos "+listadoSeleccion.size());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(listadoSeleccion.size()==0){
            JOptionPane.showMessageDialog(this,"No se ha seleccionado ningÃºn items");
        }else{
            //aca debo llamar a documento para crear el pdf con el resumen de saldo
            Encabezado encabezado=new Encabezado();
            Inquilinos inquilino=new Inquilinos();
            Propiedades propiedad=new Propiedades();
            Generable gen=new Propiedades();
            propiedad=(Propiedades) gen.Cargar(Saldos.getIdBuscador());
            
            Contratable genI=new Inquilinos();
            //gen.Cargar(WIDTH)
            inquilino=(Inquilinos)genI.CargarDesdePropiedad(propiedad.getId());
            encabezado.setFecha(Inicio.fechaDia);
            encabezado.setNombre(inquilino.getNombre());
            encabezado.setDireccion(inquilino.getDomicilioRef());
            encabezado.setIdComprobante("Resumen de Saldo");
            encabezado.setLocalidad(Inicio.configuracion.getLocalidad());
            encabezado.setNumeroDeCuit(inquilino.getCuit());
            encabezado.setTelefono(inquilino.getTelefono());
            encabezado.setTextoSuperior(Inicio.configuracion.getNombre()+"\n"+Inicio.configuracion.getDireccion());
            encabezado.setMail(inquilino.getMail());
            CuerpoDetalle cuerpo=new CuerpoDetalle();
            cuerpo.setDetalle(listadoSeleccion);
            PieDocumento pie=new PieDocumento(Inicio.configuracion.getNota1());
            
            ResumenDeSaldo resumen=new ResumenDeSaldo(encabezado,cuerpo,pie);
            resumen.start();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Iterator it=listadoSeleccion.listIterator();
        Saldos saldo=new Saldos();
        Double totalGral=0.0;
        String totalElegido="";
        Recibos recibo;
        RecibosResumen resumen;
        Generable gen=new Recibos();
        recibo=new Recibos();
        recibo.setFecha(Inicio.fechaDia);
        recibo.setIdUsuario(Inicio.usuario.getNumeroId());
        recibo.setIdPropiedad(idPropiedad);
        recibo.setIdContrato(idContrato);
        ArrayList listadoSald=new ArrayList();
        while(it.hasNext()){
            saldo=(Saldos)it.next();
            totalGral=totalGral + saldo.getTotal();
            resumen=new RecibosResumen();
            resumen.setDescripcion("Resumen NÂ° "+saldo.getIdResumen()+" monto :"+saldo.getSaldo()+" recargo :"+saldo.getRecargo());
            resumen.setIdResumen(saldo.getIdResumen());
            resumen.setRecargo(saldo.getRecargo());
            resumen.setMonto(saldo.getTotal());
            resumen.setSaldoOriginal(saldo.getSaldo());
            resumen.setSaldo(0.00);
            listadoSald.add(resumen);
        }
        recibo.setMonto(totalGral);
        recibo.setResumenRecibos(listadoSald);
        totalElegido=String.valueOf(JOptionPane.showInputDialog(this,"Ingrese numero a imprimir ","0"));
        recibo.setNumero(Integer.parseInt(totalElegido));
        gen.Alta(recibo);
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

